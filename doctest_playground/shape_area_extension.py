def triangle (height , base):
    '''
    >>> triangle(3,4)
    6.0
    '''
    return 0.5 * height * base

def square (side):
    '''
    :param side:
    :return:
    >>> rectangle(3,4)
    12
    '''
    return side*side

def rectangle(length,breadth):
    '''
    :param length:
    :param breadth:
    :return:
    >>> square(3)
    9
    '''
    return length * breadth